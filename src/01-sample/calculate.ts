export function sum(num1: number, num2: number): number {
  return num1 + num2;
}

export function minus(num1: number, num2: number): number {
  return num1 - num2;
}

export function divide(num1: number, num2: number): number {
  return num1 / num2;
}

export function multiply(num1: number, num2: number): number {
  return num1 * num2;
}

export function pow(num1: number, num2: number): number {
  return Math.pow(num1, num2);
}
